package com.fingerprint.object;

public class FingerPrintFormParam {

	private String sessionID;
	private String browserFP;
	private String deviceFP;
	public String getSessionID() {
		return sessionID;
	}
	public void setSessionID(String sessionID) {
		this.sessionID = sessionID;
	}
	public String getBrowserFP() {
		return browserFP;
	}
	public void setBrowserFP(String browserFP) {
		this.browserFP = browserFP;
	}
	public String getDeviceFP() {
		return deviceFP;
	}
	public void setDeviceFP(String deviceFP) {
		this.deviceFP = deviceFP;
	}
	
	
	
	
}
