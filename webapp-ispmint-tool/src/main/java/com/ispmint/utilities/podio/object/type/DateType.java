package com.ispmint.utilities.podio.object.type;

public class DateType {

	private String start;
	private String start_date_utc;
	private String start_time_utc;
	private String start_time;
	private String start_utc;
	private String start_date;
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getStart_date_utc() {
		return start_date_utc;
	}
	public void setStart_date_utc(String start_date_utc) {
		this.start_date_utc = start_date_utc;
	}
	public String getStart_time_utc() {
		return start_time_utc;
	}
	public void setStart_time_utc(String start_time_utc) {
		this.start_time_utc = start_time_utc;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	public String getStart_utc() {
		return start_utc;
	}
	public void setStart_utc(String start_utc) {
		this.start_utc = start_utc;
	}
	public String getStart_date() {
		return start_date;
	}
	public void setStart_date(String start_date) {
		this.start_date = start_date;
	}
	
	
}
